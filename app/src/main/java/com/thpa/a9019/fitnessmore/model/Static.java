public class Static {
    int count;
    String name;

    public Static(int count,String name){
        this.count = count;
        this.name  = name;
    }
    public int getCount() {
        return count;
    }
    public void setCount(int count) {
        this.count = count;
    }
    public int getName() {
        return count;
    }
    public void setName(String name) {
        this.name = name ;
    }


}
